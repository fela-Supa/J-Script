// Arrays - we use them to store large sets of data/lists
// Arrays use index to track items (n = n -1)

// Array definition
let array = ["item1", "item2", "item3", "item4", "item5"]
console.log(array);



// array methods - shift
let vehicles = ["BMW", "Mercedes", "Toyota", "Volvo"];
vehicles.shift();
console.log(vehicles);

// array methods - push
let vehicle = ["BMW", "Mercedes", "Toyota", "Volvo"];
vehicle.push("LandRover");
console.log(vehicle);

// array methods - slice
let cars = ["BMW", "Mercedes", "Toyota", "Volvo"];
let newCars = cars.slice(1, 3);
console.log(newCars);

